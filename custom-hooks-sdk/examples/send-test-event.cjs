#!/usr/bin/env node
/**
 * Smoke-test the Custom Hooks API so you can confirm your backend receives events.
 *
 * Prerequisites: from the `custom-hooks-sdk` directory, run `yarn build` once.
 *
 * Loads environment variables from `custom-hooks-sdk/.env` if present. Variables
 * already set in the shell are not overwritten.
 *
 * Usage:
 *   Create `custom-hooks-sdk/.env` with PERMISO_API_KEY=... then:
 *   yarn example:send
 *
 * Or override for one run:
 *   PERMISO_API_KEY=your-secret yarn example:send
 *
 * Optional env:
 *   PERMISO_BASE_URL  — override API base (default: https://alb.permiso.io)
 *   SKIP_END_RUN      — set to "1" to skip the final `stop` event
 */

const path = require("path");
require("dotenv").config({ path: path.join(__dirname, "..", ".env") });

const { PermisoCustomHooksClient, PermisoCustomHooksError } = require(
  path.join(__dirname, "..", "lib", "index.js"),
);

async function main() {
  const apiKey = process.env.PERMISO_API_KEY;
  if (!apiKey || apiKey.trim() === "") {
    console.error(
      "Missing PERMISO_API_KEY. Add it to custom-hooks-sdk/.env, export it in the shell, or pass it for one run: PERMISO_API_KEY=... yarn example:send",
    );
    process.exit(1);
  }

  const baseUrl = process.env.PERMISO_BASE_URL;
  const config = baseUrl ? { apiKey, baseUrl } : { apiKey };
  const client = new PermisoCustomHooksClient(config);

  const resolvedBase = baseUrl ?? "https://alb.permiso.io";
  console.log(`Posting to ${resolvedBase.replace(/\/$/, "")}/hooks\n`);

  try {
    console.log("runId (generated by client):", client.getRunId());

    const ping = await client.sendEvent("sdk_example_ping", {
      source: "user",
      type: "text",
      text: "Hello, world!",
    });
    console.log("\nsdk_example_ping:", JSON.stringify(ping, null, 2));

    if (process.env.SKIP_END_RUN === "1") {
      console.log("\nSKIP_END_RUN=1 — not sending stop.");
      return;
    }

    const stop = await client.endRun();
    console.log("\nstop (endRun):", JSON.stringify(stop, null, 2));
    console.log("new runId after endRun:", client.getRunId());
    console.log("\nDone. If your backend is wired correctly, you should see these events there.");
  } catch (err) {
    if (err instanceof PermisoCustomHooksError) {
      console.error("\nPermisoCustomHooksError:", err.message);
      if (err.status !== undefined) console.error("HTTP status:", err.status);
      if (err.body !== undefined) console.error("Body:", err.body);
    } else {
      console.error(err);
    }
    process.exit(1);
  }
}

main();
